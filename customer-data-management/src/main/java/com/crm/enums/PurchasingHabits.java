package com.crm.enums;

/**
 * Enumeration representing different purchasing habits of customers.
 */
public enum PurchasingHabits {
	/**
	 * New customer with no or very few purchases.
	 */
	NEW,

	/**
	 * Customer who makes purchases frequently.
	 */
	FREQUENT,

	/**
	 * Customer who makes purchases sparsely.
	 */
	SPARSE,

	/**
	 * Customer who makes purchases regularly.
	 */
	REGULAR
}